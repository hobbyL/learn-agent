def search_planets(query: str) -> list[dict]:
    """搜索候选星球，返回匹配结果列表。"""
    query_lower = query.lower()
    results = []
    for name, data in PLANETS.items():
        # 搜索名称和所有文本字段
        searchable = f"{name} {data.get('环境', '')} {data.get('type', '')}"
        for v in data.values():
            if isinstance(v, str):
                searchable += f" {v}"
            elif isinstance(v, list):
                searchable += f" {'、'.join(v)}"
        if query_lower in searchable.lower():
            results.append({
                "name": name,
                "环境": data["环境"],
                "宜居指数": data["宜居指数"],
                "防御评分": data["防御评分"],
                "经济评分": data["经济评分"],
            })
    return results


def lookup_planet(planet: str, field: str) -> str | None:
    """精确查询某星球的某个属性。"""
    if planet not in PLANETS:
        return None
    data = PLANETS[planet]
    if field not in data:
        return None
    value = data[field]
    if isinstance(value, list):
        return "、".join(str(item) for item in value)
    return str(value)


def get_planet_fields(planet: str) -> list[str] | None:
    """获取某星球的所有可查字段。"""
    if planet not in PLANETS:
        return None
    return [k for k in PLANETS[planet].keys() if k != "type"]


