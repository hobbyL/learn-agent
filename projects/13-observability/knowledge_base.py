# 查询接口 —— 供工具层调用
# ============================================================

def search_entities(query: str) -> list[dict[str, str]]:
    """
    模糊搜索知识库，返回匹配的实体摘要列表。

    搜索逻辑：query 中的关键词出现在实体名、描述、或任何字段值中即匹配。
    返回格式：[{"name": "实体名", "type": "类型", "summary": "描述前50字"}]

    为什么返回摘要而不是完整数据？
        模拟真实搜索引擎行为——先给概览，需要详情再用 lookup 精确查询。
        这迫使 ReAct Agent 走"search → 选择实体 → lookup 具体字段"的多步链路。
    """
    query_lower = query.lower()
    results = []

    for name, data in KNOWLEDGE_BASE.items():
        # 在实体名中搜索
        if query_lower in name.lower():
            results.append(_make_summary(name, data))
            continue

        # 在所有字段值中搜索（matched 标记防止同一实体重复添加）
        matched = False
        for value in data.values():
            if matched:
                break
            if isinstance(value, str) and query_lower in value.lower():
                results.append(_make_summary(name, data))
                matched = True
            elif isinstance(value, list):
                for item in value:
                    if isinstance(item, str) and query_lower in item.lower():
                        results.append(_make_summary(name, data))
                        matched = True
                        break

    return results


def lookup_entity(entity: str, field: str) -> str | int | float | list[str] | None:
    """
    精确查询某个实体的某个字段。

    参数：
        entity: 实体名（必须完全匹配知识库中的键名）
        field: 字段名（如"人口"、"年龄"、"导师"等）

    返回：
        字段值，如果实体或字段不存在则返回 None

    为什么要精确匹配？
        模拟数据库查询——search 是模糊的，lookup 是精确的。
        LLM 必须从 search 结果中拿到准确的实体名，才能 lookup 成功。
        这训练 LLM 的"信息串接"能力。
    """
    data = KNOWLEDGE_BASE.get(entity)
    if data is None:
        return None
    return data.get(field)


def compare_entities(entity_a: str, entity_b: str, field: str) -> dict[str, any] | None:
    """
    比较两个实体的同一字段值。

    返回格式：
        {"entity_a": name, "value_a": val, "entity_b": name, "value_b": val, "field": field}
        如果任一实体/字段不存在，返回 None

    为什么单独做一个 compare 工具？
        虽然理论上可以 lookup 两次再让 LLM 自己比较，
        但 compare 工具能测试 LLM 是否会"聪明地"选择更高效的工具——
        一步 compare 还是两步 lookup？观察 ReAct 和 Direct 模式的选择差异。
    """
    data_a = KNOWLEDGE_BASE.get(entity_a)
    data_b = KNOWLEDGE_BASE.get(entity_b)

    if data_a is None or data_b is None:
        return None

    val_a = data_a.get(field)
    val_b = data_b.get(field)

    if val_a is None or val_b is None:
        return None

    return {
        "entity_a": entity_a,
        "value_a": val_a,
        "entity_b": entity_b,
        "value_b": val_b,
        "field": field,
    }


def _make_summary(name: str, data: dict) -> dict[str, str]:
    """生成实体的搜索摘要（名字+类型+描述前50字）。"""
    entity_type = data.get("类型", "未知")
    desc = data.get("描述", "")
    summary = desc[:50] + "..." if len(desc) > 50 else desc
    return {"name": name, "type": entity_type, "summary": summary}


