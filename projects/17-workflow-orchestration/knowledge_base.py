def get_energy_options() -> str:
    """获取能源方案评估。"""
    energy = PLANET_DATA["能源"]
    lines = ["蓝晶星能源方案评估：\n"]
    for name, data in energy.items():
        lines.append(f"【{name}】")
        lines.append(f"  可行性：{data['可行性']}")
        if "年均光照" in data:
            lines.append(f"  年均光照：{data['年均光照']}")
        if "效率" in data:
            lines.append(f"  效率：{data['效率']}")
        if "优势" in data:
            lines.append(f"  优势：{data['优势']}")
        if "风险" in data:
            lines.append(f"  风险：{data['风险']}")
        lines.append(f"  建议：{data['建议']}\n")
    return "\n".join(lines)


