def search_entities(query: str) -> list[dict]:
    """
    模糊搜索：在实体名称和所有字段值中查找匹配项。
    返回匹配实体的摘要列表（不含完整数据，迫使调用方再用 lookup_entity）。
    """
    query_lower = query.lower()
    results = []

    for name, data in KNOWLEDGE_BASE.items():
        # 名字命中
        if query_lower in name.lower():
            results.append({
                "名称": name,
                "类型": data.get("类型", "未知"),
                "摘要": _summarize(name, data),
            })
            continue
        # 字段值命中
        for field, value in data.items():
            if isinstance(value, str) and query_lower in value.lower():
                results.append({
                    "名称": name,
                    "类型": data.get("类型", "未知"),
                    "摘要": _summarize(name, data),
                })
                break

    return results if results else [{"error": f"未找到与 '{query}' 相关的实体"}]


def lookup_entity(name: str, field: str = "") -> str:
    """
    精确查询实体。
    - field 为空：返回实体全部属性
    - field 非空：返回指定字段值
    """
    if name not in KNOWLEDGE_BASE:
        # 尝试模糊匹配提示
        similar = [k for k in KNOWLEDGE_BASE if name in k or k in name]
        if similar:
            return f"未找到 '{name}'，你是否在找：{', '.join(similar)}？"
        return f"未找到实体 '{name}'。可用实体：{', '.join(KNOWLEDGE_BASE.keys())}"

    data = KNOWLEDGE_BASE[name]

    if not field:
        # 返回全部属性
        attrs = "；".join(f"{k}: {v}" for k, v in data.items())
        return f"{name} 的全部属性 —— {attrs}"

    if field in data:
        return f"{name} → {field}: {data[field]}"

    return f"实体 '{name}' 没有 '{field}' 属性。可用属性：{', '.join(data.keys())}"


def compare_entities(name1: str, name2: str, field: str) -> str:
    """
    比较两个实体的同一字段值。
    数值型字段额外给出大小关系。
    """
    if name1 not in KNOWLEDGE_BASE:
        return f"未找到实体 '{name1}'"
    if name2 not in KNOWLEDGE_BASE:
        return f"未找到实体 '{name2}'"

    data1 = KNOWLEDGE_BASE[name1]
    data2 = KNOWLEDGE_BASE[name2]

    if field not in data1:
        return f"'{name1}' 没有 '{field}' 属性"
    if field not in data2:
        return f"'{name2}' 没有 '{field}' 属性"

    val1 = data1[field]
    val2 = data2[field]
    result = f"{name1} 的 {field}: {val1}\n{name2} 的 {field}: {val2}"

    # 数值比较增加大小判断
    if isinstance(val1, (int, float)) and isinstance(val2, (int, float)):
        diff = val1 - val2
        if diff > 0:
            result += f"\n{name1} 比 {name2} 多/大 {abs(diff)}"
        elif diff < 0:
            result += f"\n{name2} 比 {name1} 多/大 {abs(diff)}"
        else:
            result += "\n两者相同"

    return result


def _summarize(name: str, data: dict) -> str:
    """生成实体的单行摘要，供 search 返回使用。"""
    entity_type = data.get("类型", "未知")
    if entity_type == "院系":
        return f"{name}（{entity_type}）— 院长: {data.get('院长', '?')}，研究方向: {data.get('研究方向', '?')[:15]}..."
    elif entity_type in ("学员", "导师", "院长"):
        return f"{name}（{entity_type}）— 院系: {data.get('院系', '?')}，专长: {data.get('专长', '?')[:20]}..."
    elif entity_type == "合作机构":
        return f"{name}（{entity_type}）— 负责人: {data.get('负责人', '?')}，职能: {data.get('职能', '?')[:20]}..."
    elif entity_type == "人物":
        return f"{name}（{entity_type}）— {data.get('职位', '?')}"
    return f"{name}（{entity_type}）"


